// ── TTS Engine ────────────────────────────────────────────────────
const TTS = {
  speaking: false,
  currentUtterance: null,
  voicesLoaded: false,

  init() {
    if (this.voicesLoaded) return;
    if ('speechSynthesis' in window) {
      speechSynthesis.getVoices();
      speechSynthesis.onvoiceschanged = () => { this.voicesLoaded = true; };
    }
  },

  getBestVoice(langCode) {
    const voices = speechSynthesis.getVoices();
    const lang = langCode.split('-')[0];

    const google = voices.find(v => v.lang.startsWith(lang) && v.name.toLowerCase().includes('google'));
    if (google) return google;

    const ms = voices.find(v => v.lang.startsWith(lang) && v.name.toLowerCase().includes('microsoft'));
    if (ms) return ms;

    const exact = voices.find(v => v.lang === langCode);
    if (exact) return exact;

    return voices.find(v => v.lang.startsWith(lang)) || null;
  },

  speak(text, lang = 'en-IN') {
    if (!('speechSynthesis' in window)) return false;
    this.stop();

    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = lang;

    if (lang.startsWith('hi')) {
      utterance.rate = 0.88;
      utterance.pitch = 1.05;
    } else {
      utterance.rate = 0.95;
      utterance.pitch = 1;
    }
    utterance.volume = 1;

    const voice = this.getBestVoice(lang);
    if (voice) utterance.voice = voice;

    utterance.onstart = () => { this.speaking = true; };
    utterance.onend = () => { this.speaking = false; this.currentUtterance = null; };
    utterance.onerror = () => { this.speaking = false; this.currentUtterance = null; };

    this.currentUtterance = utterance;
    speechSynthesis.speak(utterance);
    return true;
  },

  stop() {
    if ('speechSynthesis' in window && speechSynthesis.speaking) speechSynthesis.cancel();
    this.speaking = false;
    this.currentUtterance = null;
  },

  toggle(text, lang = 'en-IN') {
    if (this.speaking) { this.stop(); return false; }
    this.speak(text, lang);
    return true;
  },

  schemeToSpeech(scheme, lang = 'en') {
    if (lang === 'hi') {
      // Look up full Hindi translation
      const hi = HINDI[scheme.name];
      if (hi) {
        const benefits = hi.benefits?.length ? `इस योजना के लाभ हैं। ${hi.benefits.join('। ')}` : '';
        const eligibility = hi.eligibility?.length ? `पात्रता। ${hi.eligibility.join('। ')}` : '';
        return `${hi.name}। ${hi.desc}। ${benefits}। ${eligibility}`;
      }
      // Fallback: speak English content with Hindi voice
      return `${scheme.name}। ${scheme.description}`;
    }

    const benefits = scheme.benefits?.length ? `Benefits include: ${scheme.benefits.join('. ')}` : '';
    const eligibility = scheme.eligibility?.length ? `Eligibility: ${scheme.eligibility.join('. ')}` : '';
    return `${scheme.name}. ${scheme.description}. ${benefits}. ${eligibility}`;
  }
};

TTS.init();
export default TTS;
